
package cadastro;

public class Agenda {
    
    private int id;
    private String nome;
    private String cpf;
    private int cliente_id;
    private String tel_resid,fone,tel_cel;
    

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the cliente_id
     */
    public int getCliente_id() {
        return cliente_id;
    }

    /**
     * @param cliente_id the cliente_id to set
     */
    public void setCliente_id(int cliente_id) {
        this.cliente_id = cliente_id;
    }

    /**
     * @return the tel_resid
     */
    public String getTel_resid() {
        return tel_resid;
    }

    /**
     * @param tel_resid the tel_resid to set
     */
    public void setTel_resid(String tel_resid) {
        this.tel_resid = tel_resid;
    }

    /**
     * @return the fone
     */
    public String getFone() {
        return fone;
    }

    /**
     * @param fone the fone to set
     */
    public void setFone(String fone) {
        this.fone = fone;
    }

    /**
     * @return the tel_cel
     */
    public String getTel_cel() {
        return tel_cel;
    }

    /**
     * @param tel_cel the tel_cel to set
     */
    public void setTel_cel(String tel_cel) {
        this.tel_cel = tel_cel;
    }

  
    
    
}
